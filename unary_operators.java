// Unary Operators need only one operand. 
// They are used to increment, decrement, or negate a value.
public class unary_operators {
   public static void main(String[]args){
      int a = 10;
      System.out.println(a);  // here normally print
      a++; // here a++ means a = a+1 = 11
      System.out.println(a);  // and now the ans is 11
      a--; // here a++ means a = a-1 = 10 
      System.out.println(a);
    }
}